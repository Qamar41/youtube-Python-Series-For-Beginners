                                     # Operators in Python

'''Python Operator falls into 7 categories:

i)Python Arithmetic Operator
ii)Python Relational Operator
iii)Python Assignment Operator 
iv)Python Logical Operator
v)Python Membership Operator  (in , not in)
vi)Python Identity Operator  (is , is not)
vii)Python Bitwise Operator     '''



# Python Arithmetic Operator

# a. Addition(+)
'''Adds the values on either side of the operator.'''
3+4

# b. Subtraction(-) 
'''Subtracts the value on the right from the one on the left.'''
3+4

# Multiplication(*)
'''Multiplies the values on either side of the operator.'''
3*4

# d. Division(/)
'''Divides the value on the left by the one on the right.
 Notice that division results in a floating-point value.'''

# e. Exponentiation(**)
'''Raises the first number to the power of the second.'''
3**4

# f. Floor Division(//)

'''Divides and returns the integer value of the quotient.
 It dumps the digits after the decimal.'''
10//3
#  output : 3

# g. Modulus(%)

'''Divides and returns the value of the remainder.'''
print(4%3)

# output : 1

