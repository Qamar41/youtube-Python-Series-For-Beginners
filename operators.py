    # Python Logical Operator

# a. and

'''If the conditions on both the sides of the operator are true,
 then the expression as a whole is true.'''

a=7>7 and 2>-1
print(a)

# output : False

# b. or

'''The expression is false only if both the statements around the operator are false.
 Otherwise, it is true.'''

a=7>7 or 2>-1
print(a)

# output : True



